package OPPs2.Import_Package.Package;

/**
 * 📦 What is a User-Defined Package?
 * 👉 A user-defined package is a package we create ourselves to organize related classes.
 *
 * ✅ Why do we use them?
 * - To make code modular and manageable
 * - To make maintenance and reusability easier
 * - To keep structure clean in large projects
 *
 * 💡 Summary Table:
 * Step             | Description
 * -----------------|------------------------------------------------
 * package keyword  | To declare your own package
 * Folder structure | Must match the package name (e.g., mypackage)
 * import keyword   | To use classes from a user-defined package
 * Compile order    | First compile the package class, then the main class
 */
public class UserDefinedPackage {
    public static void main(String[] args) {
        // Output message for demonstration
        System.out.println("Welcome (🙏) to User-defined Package..");
    }
}
