package ControlStatement_Math_String.Math_Class;

public class Min {
    public static void main(String[] args) {
        int a = 15, b = 25;
        double x = 4.8, y = 2.3;

        // Using Math.min()
        System.out.println("Min of " + a + " and " + b + " is: " + Math.min(a, b)); // Output: 15
        System.out.println("Min of " + x + " and " + y + " is: " + Math.min(x, y)); // Output: 2.3
    }
}
