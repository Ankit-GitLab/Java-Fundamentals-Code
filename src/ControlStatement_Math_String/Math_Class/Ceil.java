package ControlStatement_Math_String.Math_Class;

public class Ceil {
    public static void main(String[] args) {
        double x = 5.3;
        double y = -3.8;

        // Using Math.ceil()
        System.out.println("Ceil of " + x + " is: " + Math.ceil(x));  // Output: 6.0
        System.out.println("Ceil of " + y + " is: " + Math.ceil(y));  // Output: -3.0
    }
}
