package ControlStatement_Math_String.Math_Class;

public class Sqrt {
    public static void main(String[] args) {
        double x = 25;
        double y = -9;

        // Using Math.sqrt()
        System.out.println("Square root of " + x + " is: " + Math.sqrt(x)); // Output: 5.0
        System.out.println("Square root of " + y + " is: " + Math.sqrt(y)); // Output: NaN
    }
}
