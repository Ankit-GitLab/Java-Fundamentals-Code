package ControlStatement_Math_String.Math_Class;

public class Floor {
    public static void main(String[] args) {
        double a = 5.9;
        double b = -4.2;

        // Using Math.floor()
        System.out.println("Floor of " + a + " is: " + Math.floor(a)); // Output: 5.0
        System.out.println("Floor of " + b + " is: " + Math.floor(b)); // Output: -5.0
    }
}
