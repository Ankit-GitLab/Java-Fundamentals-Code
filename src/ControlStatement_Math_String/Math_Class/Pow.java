package ControlStatement_Math_String.Math_Class;

public class Pow {
    public static void main(String[] args) {
        double base = 2;
        double exponent = 5;

        // Using Math.pow()
        double result = Math.pow(base, exponent);

        System.out.println(base + " raised to the power " + exponent + " is: " + result);
    }
}
