package ControlStatement_Math_String.Math_Class;

public class Abs {
    public static void main(String[] args) {
        int a = -10;
        double b = -5.67;

        // Using Math.abs() to get absolute values
        System.out.println("Absolute of a: " + Math.abs(a));   // Output: 10
        System.out.println("Absolute of b: " + Math.abs(b));   // Output: 5.67
    }
}
