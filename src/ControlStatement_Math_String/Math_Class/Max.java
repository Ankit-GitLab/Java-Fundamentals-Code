package ControlStatement_Math_String.Math_Class;

public class Max {
    public static void main(String[] args) {
        int a = 10, b = 20;
        double x = 3.7, y = 2.9;

        // Using Math.max()
        System.out.println("Max of " + a + " and " + b + " is: " + Math.max(a, b)); // Output: 20
        System.out.println("Max of " + x + " and " + y + " is: " + Math.max(x, y)); // Output: 3.7
    }
}
