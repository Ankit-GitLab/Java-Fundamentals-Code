package OOPs.Import_Package.Import;

/**
 * 📥 **Import Statement in Java**
 * 🔹 **Definition:**
 * Import statement ka use hum dusre package ke classes ko apne current class
 * mein access karne ke liye karte hain.
 *
 * 🔄 **Types of Import:**
 * ✅ Agar **specific class** import karna ho toh `ClassName` likhte hain.
 * ✅ Agar **saare classes** ko ek package se import karna ho, toh `*` ka use karte hain.
 *
 * 🧠 **Why Use Import Statement?**
 * 1. **Code reusability** — aap kisi aur class ko apni class mein use kar sakte hain.
 * 2. **Avoid long class names** — agar aapne import kiya hai, toh aapko poora package path
 *    likhne ki zaroorat nahi hoti.
 * 3. **Improves readability** — code clean aur easy to understand hota hai.
 */

public class Statement {
    public static void main(String[] args) {
        System.out.println("Welcome (🙏) to Import statement..");
    }
}
