package BasicsOfJava;
public class PercentageOfFiveSubject {
    public static void main(String[] args) {
        double m1 = 94; // math marks
        double m2 = 92; // chemistry marks
        double m3 = 97; // physics marks
        double m4 = 99; // english marks
        double m5 = 87; // hindi marks 
        double y = (m1 + m2 + m3 + m4 + m5) / 5;
        System.out.println("Percentage of five subject : "+y);
    }
}
